package mat.client.shared;

/**
 * The Interface HasVisible.
 */
public interface HasVisible {
	
	/**
	 * Checks if is visible.
	 * 
	 * @return true, if is visible
	 */
	public boolean isVisible();
	
	/**
	 * Sets the visible.
	 * 
	 * @param visible
	 *            the new visible
	 */
	public void setVisible(boolean visible);
}

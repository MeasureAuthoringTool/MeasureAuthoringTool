package mat.model;

/**
 * The Class MatFlag.
 */
public class MatFlag {
	
	/** The id. */
	private String id;
	
	/** The flag. */
	private String flag;

	/**
	 * Gets the flag.
	 * 
	 * @return the flag
	 */
	public String getFlag() {
		return flag;
	}

	/**
	 * Sets the flag.
	 * 
	 * @param flag
	 *            the new flag
	 */
	public void setFlag(String flag) {
		this.flag = flag;
	}

	/**
	 * Sets the id.
	 * 
	 * @param id
	 *            the new id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public String getId() {
		return id;
	}
}

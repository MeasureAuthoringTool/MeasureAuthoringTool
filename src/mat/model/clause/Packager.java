package mat.model.clause;

/**
 * The Class Packager.
 */
public class Packager {
	
	/** The id. */
	private String id;
	
	/** The measure. */
	private Measure measure;
	
	/** The sequence. */
	private int sequence;
	
	
	/**
	 * Gets the id.
	 * 
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 * 
	 * @param id
	 *            the new id
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * Gets the measure.
	 * 
	 * @return the measure
	 */
	public Measure getMeasure() {
		return measure;
	}
	
	/**
	 * Sets the measure.
	 * 
	 * @param measure
	 *            the new measure
	 */
	public void setMeasure(Measure measure) {
		this.measure = measure;
	}
	
	/**
	 * Gets the sequence.
	 * 
	 * @return the sequence
	 */
	public int getSequence() {
		return sequence;
	}
	
	/**
	 * Sets the sequence.
	 * 
	 * @param sequence
	 *            the new sequence
	 */
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	
	
}
